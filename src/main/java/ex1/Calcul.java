package ex1;


public class Calcul {

    /**
     *  Méthode Addition
     * @param a
     * @param b
     * @return
     */
    public int addition(int a, int b){
        return a+b;
    }

    /**
     * Méthode soustraction
     * @param a
     * @param b
     * @return
     */
    public int soustraction(int a, int b){
        return a-b;
    }
}
